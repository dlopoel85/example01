public class HomeorkIf {

    public static void main(String[] args) {

        char letter = '/';

        int a = 9;
        int b = 4;

        if (letter == '+') {
            int c = a + b;
            System.out.println("a + b = " + c);
        }
        if (letter == '-') {
            int c = a - b;
            System.out.println("a - b = " + c);
        }
        if (letter == '*') {
            int c = a * b;
            System.out.println("a * b = " + c);
        }
        if (letter == '/') {
            int c = a / b;
            System.out.println("a / b = " + c);
        }


        System.out.println("-----------------------------");
    }
}
